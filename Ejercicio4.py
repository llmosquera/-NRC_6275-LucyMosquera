#Calcular el área de un triángulo equilátero ejercicio  22
#Pedimos que  ingre e imprima el lado 
lado = int(input ("Ingrese el lado: "))
#Formula del area del triangulo equilatero
a = ((3**0.5)/4) * lado**2
#Imprimimos el area del triangulo
print ("El area del triangulo equilatero es : " , a )
