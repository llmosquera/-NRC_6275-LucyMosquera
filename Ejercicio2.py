#Ingresamos e imprimimos el valor de velocidad  inicial, y float permitira ingresar un valor decimal
vi = float(input("Ingrese la velocidad inicial: "))
#Ingresamos e imprimimos el valor velocidad final, y float permitira ingresar un valor decimal
vf = float(input("Ingrese la velocidad final: "))
#Formula de la velocidad 
velocidad = (vi + vf) / 2
#Imprimimos el valor de velocidad 
print(velocidad)
